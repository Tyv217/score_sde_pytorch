    def configure_optimizers(self):

        optims = []
        scheds = []

        optimizer = torch.optim.Adam(self.model.parameters(),
                               lr=self.lr,
                               weight_decay=self.weight_decay)
        optims.append(optimizer)
        # Check if more than 1 optimizer is required (Used for adversarial training)
        try:
            if self.lr_2 is not None:
                optimizer2 = torch.optim.Adam(self.parameters(),
                                        lr=self.lr_2)
                optims.append(optimizer2)
        except:
            pass

        try:
            if self.scheduler_gamma is not None:
                scheduler = torch.optim.lr_scheduler.ExponentialLR(optims[0],
                                                             gamma = self.scheduler_gamma)
                scheds.append(scheduler)

                # Check if another scheduler is required for the second optimizer
                try:
                    if self.scheduler_gamma_2 is not None:
                        scheduler2 = torch.optim.lr_scheduler.ExponentialLR(optims[1],
                                                                      gamma = self.scheduler_gamma_2)
                        scheds.append(scheduler2)
                except:
                    pass
                return optims, scheds
        except:
            return optims